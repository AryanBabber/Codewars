function whoIsWinner(piecesPositionList) {
	dict = { A: 35, B: 36, C: 37, D: 38, E: 39, F: 40, G: 41 };
	res = new Array(42).fill("-");
	for (s of piecesPositionList) {
		res[dict[s[0]]] = s[2];
		dict[s[0]] -= 7;
		if (
			/((R.{6}){3}R)|(^((.{0,3}|.{7,10}|.{14,17}|.{21,24}|.{28,31}|.{35,38})R{4}))|(^((.{3,6}|.{10,13}|.{17,20})(R.{5}){3}R))|(^(.{0,3}|.{7,10}|.{14,17})(R.{7}){3}R)/.test(
				res.join("")
			)
		)
			return "Red";
		if (
			/((Y.{6}){3}Y)|(^((.{0,3}|.{7,10}|.{14,17}|.{21,24}|.{28,31}|.{35,38})Y{4}))|(^((.{3,6}|.{10,13}|.{17,20})(Y.{5}){3}Y))|(^(.{0,3}|.{7,10}|.{14,17})(Y.{7}){3}Y)/.test(
				res.join("")
			)
		)
			return "Yellow";
	}
	return "Draw";
}
